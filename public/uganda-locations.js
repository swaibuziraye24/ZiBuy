// ── Helper: get all districts ─────────────────
export function getDistricts() {
  return Object.keys(UGANDA_LOCATIONS).sort();
}

// ── Helper: get sub-locations for a district ──
export function getSubLocations(district) {
  return UGANDA_LOCATIONS[district] || [];
}

// ── Build HTML for a two-level location select ─
export function buildLocationSelect({
  districtId   = "district-select",
  subLocationId = "sublocation-select",
  districtLabel = "District / City",
  subLabel      = "Town / Village / Area",
  required      = false,
  selectedDistrict = "",
  selectedSub      = ""
} = {}) {

  const districts = getDistricts();

  return `
    <div style="display:flex;flex-direction:column;gap:10px">
      <div>
        <label style="display:block;font-size:12px;font-weight:800;
          color:#374151;text-transform:uppercase;letter-spacing:.5px;margin-bottom:6px">
          📍 ${districtLabel}
        </label>
        <select id="${districtId}" ${required ? "required" : ""}
          onchange="updateSubLocations('${districtId}','${subLocationId}')"
          style="width:100%;padding:13px 14px;border:1.5px solid #e5e7eb;
          border-radius:12px;font-size:14px;font-family:inherit;
          outline:none;background:white;box-sizing:border-box">
          <option value="">Select District / City</option>
          ${districts.map(d => `
            <option value="${d}" ${d === selectedDistrict ? "selected" : ""}>
              ${d}
            </option>
          `).join("")}
        </select>
      </div>

      <div id="${subLocationId}-wrap" style="${selectedDistrict ? "" : "display:none"}">
        <label style="display:block;font-size:12px;font-weight:800;
          color:#374151;text-transform:uppercase;letter-spacing:.5px;margin-bottom:6px">
          🏘️ ${subLabel}
        </label>
        <select id="${subLocationId}" ${required ? "required" : ""}
          style="width:100%;padding:13px 14px;border:1.5px solid #e5e7eb;
          border-radius:12px;font-size:14px;font-family:inherit;
          outline:none;background:white;box-sizing:border-box">
          <option value="">Select Town / Village / Area</option>
          ${getSubLocations(selectedDistrict).map(s => `
            <option value="${s}" ${s === selectedSub ? "selected" : ""}>
              ${s}
            </option>
          `).join("")}
        </select>
      </div>
    </div>
  `;
}

// ── Called when district changes ──────────────
window.updateSubLocations = function(districtId, subLocationId) {
  const district = document.getElementById(districtId)?.value;
  const subWrap  = document.getElementById(`${subLocationId}-wrap`);
  const subEl    = document.getElementById(subLocationId);
  if (!subWrap || !subEl) return;

  if (!district) {
    subWrap.style.display = "none";
    subEl.innerHTML = "<option value=''>Select Town / Village / Area</option>";
    return;
  }

  const subs = UGANDA_LOCATIONS[district] || [];

  subEl.innerHTML = `
    <option value="">Select Town / Village / Area</option>
    ${subs.map(s => `<option value="${s}">${s}</option>`).join("")}
  `;
  subWrap.style.display = "block";
};
